        public void SetText(string text)
        {
            if (this.label1.InvokeRequired)
            {
                base.Invoke(new SetTextHandler(this.SetText), new object[] { text });
            }
            else if (text == "close")
            {
                base.Close();
            }
            else
            {
                this.label1.Text = text;
            }
        }

